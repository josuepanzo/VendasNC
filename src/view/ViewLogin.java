    private void btn_AccessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AccessActionPerformed
        // TODO add your handling code here:
        if (txt_Email.getText().isEmpty() || txt_Password.getText().isEmpty()) {
            btn_Access.setEnabled(false);
        } else {
            modelUsuario.setEmailUsuario(txt_Email.getText());
            modelUsuario.setSenhaUsuario(txt_Password.getText());
            if (controllerUsuario.getValidarUsuarioController(modelUsuario)) {
                modelUsuario = controllerUsuario.getUsuarioController(txt_Email.getText());
                modelSessaoUsuario.ID = modelUsuario.getIdUsuario();
                modelSessaoUsuario.nome = modelUsuario.getNomeUsuario();
                modelSessaoUsuario.login = modelUsuario.getSenhaUsuario();
                new ViewMenuPrincipal().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Email e Senha inv√°lidos", "Falha no Acesso", JOptionPane.ERROR_MESSAGE);
            }
            btn_Access.setEnabled(true);
        }
    }//GEN-LAST:event_btn_AccessActionPerformed

