    private void clicarComEnter(java.awt.event.KeyEvent e) {
        DefaultTableModel table = (DefaultTableModel) this.tbShowProduto.getModel();
        if (e.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) {
            modelProduto = controllerProduto.retornarProdutoController(Integer.parseInt(this.txt_Inserir.getText()));
            table.addRow(new Object[]{
                table.getRowCount() + 1,
                modelProduto.getIdProduto(),
                modelProduto.getProduto(),
                quantidade,
                modelProduto.getPreco(),
                modelProduto.getPreco() * quantidade
            });
            this.txt_valorTotal.setText(String.valueOf(somarValorTotal()));
            this.txt_Inserir.setText("");
            quantidade = 1;
        }
    }

    private float somarValorTotal() {
        float soma = 0, valor = 0;
        int cont = this.tbShowProduto.getRowCount();
        for (int i = 0; i < cont; i++) {
            valor = Float.parseFloat(String.valueOf(this.tbShowProduto.getValueAt(i, 5)));
            soma += valor;
        }
        return soma;
    }

