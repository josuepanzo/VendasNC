    private void btn_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ExcluirActionPerformed
        // TODO add your handling code here:
        int linha = this.tbShowVenda.getSelectedRow();
        int idVenda = (int) this.tbShowVenda.getValueAt(linha, 0);

        /**
         * ESCLUINDO E SOMANDO O RSTOQUE
         */
        listaModelProduto = new ArrayList<>();
        listaJoinProdutoEndVendaProduto = controllerJoinProdutoEndVendaProduto.getListaJoinProdutoEndVendaProdutoController(idVenda);

        for (int i = 0; i < listaJoinProdutoEndVendaProduto.size(); i++) {
            modelProduto = new ModelProduto();
            modelProduto.setIdProduto(listaJoinProdutoEndVendaProduto.get(i).getModelProduto().getIdProduto());
            modelProduto.setEstoque(
                    listaJoinProdutoEndVendaProduto.get(i).getModelProduto().getEstoque()
                    + listaJoinProdutoEndVendaProduto.get(i).getModelVendaProduto().getQtd_produto()
            );
            listaModelProduto.add(modelProduto);
        } // FIM EXCLUSÃO E SOMA DO ESTOQUE
        if (controllerProduto.alterarEstoqueProdutoController(listaModelProduto)) {
            controllerVendaProduto.excluirVendaProdutoController(idVenda);
            if (controllerVenda.excluirVendaController(idVenda)) {
                JOptionPane.showMessageDialog(this, "Venda excluida com sucesso",
                        "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                carregarVendaCliente();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir Venda",
                        "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir Venda",
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_ExcluirActionPerformed

    private void btn_AdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AdicionarActionPerformed
        // TODO add your handling code here:
        if (this.txt_Quantidade.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Você deve preencher todos os campos", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            modelProduto = controllerProduto.retornarProdutoController(Integer.parseInt(this.txt_CodigoProduto.getText()));

            DefaultTableModel table = (DefaultTableModel) tbShowProdutoVenda.getModel();
            int cont = 0;
            double quantidade = 0;
            quantidade = Double.parseDouble(this.txt_Quantidade.getText());
            for (int i = 0; i < cont; i++) {
                table.setNumRows(0);
            }

            table.addRow(new Object[]{
                modelProduto.getIdProduto(),
                modelProduto.getProduto(),
                this.txt_Quantidade.getText(),
                modelProduto.getPreco(),
                quantidade * modelProduto.getPreco()
            });
            somarValorTotalProduto();
        }
    }//GEN-LAST:event_btn_AdicionarActionPerformed

