    private void btn_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SalvarActionPerformed
        // TODO add your handling code here:

        int codigoVenda = 0;
        double desconto = 0;

        listaModelVendaProduto = new ArrayList<>();

        if (this.txt_Desconto.getText().equals("")) {
            desconto = 0;
        } else {
            desconto = Double.parseDouble(this.txt_Desconto.getText());
        }
        if (!this.txt_IdVenda.getText().equals("")) {
            modelVenda.setIdVenda(Integer.parseInt(this.txt_IdVenda.getText()));
        }

        modelVenda.setIdCliente(Integer.parseInt(this.txt_CodigoCliente.getText()));
        try {
            modelVenda.setData_Venda(bLDatas.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())));
        } catch (Exception ex) {
            //Logger.getLogger(ViewVenda.class.getName()).log(Level.SEVERE, null, ex);
        }
        modelVenda.setValor_liquido(Double.parseDouble(this.txt_ValorTotal.getText()));
        modelVenda.setValor_bruto(Double.parseDouble(this.txt_ValorTotal.getText()) + desconto);
        modelVenda.setDesconto(desconto);

        /**
         * UTILIZANDO A CONDIÇÃO COM A VARIÁVEL alterarSalvar
         */
        if (alterarSalvar.equals("salvar")) {

            /**
             * SALVAR VENDA NO BANCO
             */
            codigoVenda = controllerVenda.salvarVendaController(modelVenda);
            if (codigoVenda > 0) {
                JOptionPane.showMessageDialog(this, "Venda salva com sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Falha ao salvar venda", "Erro", JOptionPane.ERROR);
            }

            int cont = this.tbShowProdutoVenda.getRowCount();
            for (int i = 0; i < cont; i++) {
                int codigoProduto = (int) this.tbShowProdutoVenda.getValueAt(i, 0);
                modelVendaProduto = new ModelVendaProduto();
                modelProduto = new ModelProduto();
                modelVendaProduto.setIdProduto(codigoProduto);
                modelVendaProduto.setIdVenda(codigoVenda);
                modelVendaProduto.setPreco_produto((double) this.tbShowProdutoVenda.getValueAt(i, 3));
                modelVendaProduto.setQtd_produto(Integer.parseInt(this.tbShowProdutoVenda.getValueAt(i, 2).toString()));

                /**
                 * FAZENDO BAIXA NO ESTOQUE
                 */
                modelProduto.setIdProduto(codigoProduto);
                modelProduto.setEstoque(controllerProduto.retornarProdutoController(codigoProduto).getEstoque()
                        - Integer.parseInt(this.tbShowProdutoVenda.getValueAt(i, 2).toString()));

                listaModelVendaProduto.add(modelVendaProduto);
                listaModelProduto.add(modelProduto);
            }

            /**
             * SALVAR PRODUTOS DA VENDA
             */
            if (controllerVendaProduto.salvarVendaProdutosController(listaModelVendaProduto)) {
                /**
                 * ALTERAR O ESTOQUE DE PRODUTO
                 */
                controllerProduto.alterarEstoqueProdutoController(listaModelProduto);
//                JOptionPane.showMessageDialog(this, "Produto da Venda salvo com"
//                        + " sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                limparDados();
                carregarVendaCliente();
            } else {
                JOptionPane.showMessageDialog(this, "Falha ao salvar Produto da "
                        + "venda", "Erro", JOptionPane.ERROR);
            }
            // FIM DO MÉTODO SALVAR
            this.btn_Salvar.setEnabled(false);
        } else {
            /// CONDIÇÃO ALTERAR
            int linha = this.tbShowVenda.getSelectedRow();
            codigoVenda = (int) this.tbShowVenda.getValueAt(linha, 0);

            /**
             * ESCLUINDO E SOMANDO O RSTOQUE
             */
            listaModelProduto = new ArrayList<>();
            listaJoinProdutoEndVendaProduto = controllerJoinProdutoEndVendaProduto.getListaJoinProdutoEndVendaProdutoController(codigoVenda);
            for (int i = 0; i < listaJoinProdutoEndVendaProduto.size(); i++) {
                modelProduto = new ModelProduto();
                modelProduto.setIdProduto(listaJoinProdutoEndVendaProduto.get(i).getModelProduto().getIdProduto());
                modelProduto.setEstoque(
                        listaJoinProdutoEndVendaProduto.get(i).getModelProduto().getEstoque()
                        + listaJoinProdutoEndVendaProduto.get(i).getModelVendaProduto().getQtd_produto()
                );
                listaModelProduto.add(modelProduto);
            } // FIM EXCLUSÃO E SOMA DO ESTOQUE
            if (controllerProduto.alterarEstoqueProdutoController(listaModelProduto)) {
                if (controllerVendaProduto.excluirVendaProdutoController(codigoVenda)) {
                    // if (controllerVenda.excluirVendaController(idVenda)) {
//                    JOptionPane.showMessageDialog(this, "Venda excluida com sucesso",
//                            "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                    carregarVendaCliente();
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao excluir Venda",
                            "Erro", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir Venda",
                        "Erro", JOptionPane.ERROR_MESSAGE);
            }
            
            if (controllerVenda.alterarVendaController(modelVenda)) {
                JOptionPane.showMessageDialog(this, "Venda atualizada com "
                        + "sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Falha ao atualizar a venda",
                        "Erro", JOptionPane.ERROR);
            }
            
            int cont = this.tbShowProdutoVenda.getRowCount();
            for (int i = 0; i < cont; i++) {
                int codigoProduto = (int) this.tbShowProdutoVenda.getValueAt(i, 0);
                modelVendaProduto = new ModelVendaProduto();
                modelProduto = new ModelProduto();
                modelVendaProduto.setIdProduto(codigoProduto);
                modelVendaProduto.setIdVenda(codigoVenda);
                modelVendaProduto.setPreco_produto((double) this.tbShowProdutoVenda.getValueAt(i, 3));
                modelVendaProduto.setQtd_produto(Integer.parseInt(this.tbShowProdutoVenda.getValueAt(i, 2).toString()));

                /**
                 * FAZENDO BAIXA NO ESTOQUE
                 */
                modelProduto.setIdProduto(codigoProduto);
                modelProduto.setEstoque(controllerProduto.retornarProdutoController(codigoProduto).getEstoque()
                        - Integer.parseInt(this.tbShowProdutoVenda.getValueAt(i, 2).toString()));

                listaModelVendaProduto.add(modelVendaProduto);
                listaModelProduto.add(modelProduto);
            }
            
            /**
             * SALVAR PRODUTOS DA VENDA
             */
            if (controllerVendaProduto.salvarVendaProdutosController(listaModelVendaProduto)) {
//                JOptionPane.showMessageDialog(this, "Produto da Venda salvo com"
//                        + " sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                limparDados();
                carregarVendaCliente();
            } else {
                JOptionPane.showMessageDialog(this, "Falha ao salvar Produto da "
                        + "venda", "Erro", JOptionPane.ERROR);
            }
        }
    }//GEN-LAST:event_btn_SalvarActionPerformed

