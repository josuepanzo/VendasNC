    private void preencherCodigoClientePeloComboBox() {
        modelCliente = controllerCliente.retornarClienteController(txt_NomeCliente.getSelectedItem().toString());
        this.txt_CodigoCliente.setText(modelCliente.getIdCliente() + "");
    }

    private void preencherCodigoProdutoPeloComboBox() {
        modelProduto = controllerProduto.retornarProdutoController(txt_NomeProduto.getSelectedItem().toString());
        this.txt_CodigoProduto.setText(modelProduto.getIdProduto() + "");
    }

    private void listarCliente() {
        listaModelCliente = controllerCliente.retornarListaClienteController();
        this.txt_NomeCliente.removeAllItems();
        for (int i = 0; i < listaModelCliente.size(); i++) {
            this.txt_NomeCliente.addItem(listaModelCliente.get(i).getNome());
        }
    }

    private void listarProduto() {
        listaModelProduto = controllerProduto.retornarListaProdutoController();
        this.txt_NomeProduto.removeAllItems();
        for (int i = 0; i < listaModelProduto.size(); i++) {
            this.txt_NomeProduto.addItem(listaModelProduto.get(i).getProduto());
        }
    }

    private void somarValorTotalProduto() {
        double soma = 0, valor;
        int cont = this.tbShowProdutoVenda.getRowCount();
        for (int i = 0; i < cont; i++) {
            valor = (double) this.tbShowProdutoVenda.getValueAt(i, 4);
            soma += valor;
        }
        this.txt_ValorTotal.setText(String.valueOf(soma));
        descontoValor();
    }

    private void descontoValor() {
        try {
            this.txt_ValorTotal.setText(String.valueOf(
                    Double.parseDouble(this.txt_ValorTotal.getText()) - Double.parseDouble(this.txt_Desconto.getText())));
        } catch (NumberFormatException e) {
        }
    }

    private void carregarVendaCliente() {
        DefaultTableModel table = (DefaultTableModel) tbShowVenda.getModel();
        listaModelVendaCliente = controllerVendaCliente.getListaVendaClienteController();
        int cont = listaModelVendaCliente.size();
        table.setNumRows(0);
        for (int i = 0; i < cont; i++) {
            table.addRow(new Object[]{
                listaModelVendaCliente.get(i).getModelVenda().getIdVenda(),
                listaModelVendaCliente.get(i).getModelCliente().getNome(),
                listaModelVendaCliente.get(i).getModelVenda().getData_Venda()
            });
        }
    }

    private void limparDados() {
        this.txt_Quantidade.setText("");
        this.txt_ValorTotal.setText("");
        this.txt_Desconto.setText("");
        DefaultTableModel table = (DefaultTableModel) tbShowProdutoVenda.getModel();
        table.setNumRows(0);
    }

